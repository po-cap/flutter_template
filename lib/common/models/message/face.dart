/// 图片表情
class CustomFaceModel {
  // 资源id
  int? faceId;
  // 菜单图
  String? menuPic;
  // 图片列表
  List<String>? picList = [];

  CustomFaceModel({
    this.faceId,
    this.menuPic,
    this.picList,
  });

  Map<String, dynamic> toJson() => {
        'faceId': faceId,
        'menuPic': menuPic,
        'picList': picList,
      };
}

